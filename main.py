"""
FastAPI Crop Recommendation API
================================
Loads the pre-trained crop classifier (crop_classifier.pkl) and exposes
a single POST /predict endpoint that returns the recommended crop name.

Input  : 7 soil/weather features – N, P, K, temperature, humidity, ph, rainfall
Output : { "predicted_crop": "rice" }
"""

import os
import logging
from contextlib import asynccontextmanager

import joblib
import numpy as np
import pandas as pd

from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel, Field, field_validator

# ---------------------------------------------------------------------------
# Logging
# ---------------------------------------------------------------------------
logging.basicConfig(level=logging.INFO, format="%(levelname)s | %(name)s | %(message)s")
logger = logging.getLogger("crop_api")

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
BASE_DIR = os.path.dirname(os.path.abspath(__file__))
MODEL_PATH = os.path.join(BASE_DIR, "crop_classifier.pkl")

# ---------------------------------------------------------------------------
# App-level state (loaded once at startup)
# ---------------------------------------------------------------------------
app_state: dict = {}


@asynccontextmanager
async def lifespan(app: FastAPI):
    """Load the model once when the server starts and release on shutdown."""
    logger.info("Loading crop classifier model from: %s", MODEL_PATH)
    if not os.path.exists(MODEL_PATH):
        raise FileNotFoundError(
            f"Model file not found at '{MODEL_PATH}'. "
            "Please place 'crop_classifier.pkl' in the same directory as main.py."
        )
    app_state["model"] = joblib.load(MODEL_PATH)
    logger.info("Model loaded successfully: %s", type(app_state["model"]))
    yield
    app_state.clear()
    logger.info("Model released.")


# ---------------------------------------------------------------------------
# FastAPI app
# ---------------------------------------------------------------------------
app = FastAPI(
    title="Crop Recommendation API",
    description=(
        "Predicts the most suitable crop to grow based on soil nutrients "
        "and local weather conditions."
    ),
    version="1.0.0",
    lifespan=lifespan,
)

# Allow all origins for development – tighten this in production.
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["*"],
    allow_headers=["*"],
)


# ---------------------------------------------------------------------------
# Request / Response schemas
# ---------------------------------------------------------------------------
class CropInput(BaseModel):
    """Soil and weather parameters required for crop recommendation."""

    N: float = Field(..., ge=0, le=200, description="Nitrogen ratio in the soil (kg/ha)")
    P: float = Field(..., ge=0, le=200, description="Phosphorus ratio in the soil (kg/ha)")
    K: float = Field(..., ge=0, le=250, description="Potassium ratio in the soil (kg/ha)")
    temperature: float = Field(..., ge=-10, le=60, description="Temperature in Celsius")
    humidity: float = Field(..., ge=0, le=100, description="Relative humidity in %")
    ph: float = Field(..., ge=0, le=14, description="Soil pH value (0 – 14)")
    rainfall: float = Field(..., ge=0, le=500, description="Rainfall in mm")

    @field_validator("ph")
    @classmethod
    def ph_range(cls, v: float) -> float:
        if not (0 <= v <= 14):
            raise ValueError("pH must be between 0 and 14.")
        return v

    model_config = {
        "json_schema_extra": {
            "example": {
                "N": 90,
                "P": 42,
                "K": 43,
                "temperature": 20.87,
                "humidity": 82.0,
                "ph": 6.5,
                "rainfall": 202.93,
            }
        }
    }


class CropPrediction(BaseModel):
    """Prediction result returned by the API."""

    predicted_crop: str
    confidence_note: str = "Prediction generated by the trained classifier."


# ---------------------------------------------------------------------------
# Endpoints
# ---------------------------------------------------------------------------
@app.get("/", summary="Root")
def root():
    """Root endpoint."""
    return {"status": "ok", "message": "Crop Recommendation API is running."}


@app.get("/health", summary="Health check")
def health():
    """Railway health-check endpoint."""
    return {"status": "ok"}


@app.post(
    "/predict",
    response_model=CropPrediction,
    summary="Predict the best crop to grow",
    tags=["Prediction"],
)
def predict(data: CropInput):
    """
    ### Predict the recommended crop

    Supply the 7 soil/weather parameters and receive the name of the crop
    that is most suitable for those conditions.

    **Parameters:**
    - **N** – Nitrogen content (kg/ha)
    - **P** – Phosphorus content (kg/ha)
    - **K** – Potassium content (kg/ha)
    - **temperature** – Temperature (°C)
    - **humidity** – Relative Humidity (%)
    - **ph** – Soil pH
    - **rainfall** – Rainfall (mm)

    **Returns:**
    - `predicted_crop`: The recommended crop name (lower-case string)
    """
    model = app_state.get("model")
    if model is None:
        raise HTTPException(status_code=503, detail="Model is not loaded. Please restart the server.")

    try:
        # Build a DataFrame that matches the column order used during training
        features = pd.DataFrame(
            [[data.N, data.P, data.K, data.temperature, data.humidity, data.ph, data.rainfall]],
            columns=["N", "P", "K", "temperature", "humidity", "ph", "rainfall"],
        )

        logger.info("Received prediction request: %s", features.to_dict(orient="records"))

        prediction: np.ndarray = model.predict(features)
        crop_name: str = str(prediction[0]).strip().lower()

        logger.info("Predicted crop: %s", crop_name)
        return CropPrediction(predicted_crop=crop_name)

    except Exception as exc:
        logger.exception("Prediction failed: %s", exc)
        raise HTTPException(
            status_code=500,
            detail=f"Prediction error: {str(exc)}",
        ) from exc
